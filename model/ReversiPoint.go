// Package model リバーシに関する構造体をまとめるパッケージ。
package model

////////////////////////////////////////////////////////////////////////////////
///	@class		ReversiPoint
///	@brief		リバーシポイントクラス
///
////////////////////////////////////////////////////////////////////////////////
type ReversiPoint struct {
	X int //!< X座標
	Y int //!< Y座標
}

////////////////////////////////////////////////////////////////////////////////
///	@brief			コンストラクタ
///	@fn				NewReversiPoint() *ReversiPoint
///	@return			*ReversiPoint
///	@author			Yuta Yoshinaga
///	@date			2020.07.06
///
////////////////////////////////////////////////////////////////////////////////
func NewReversiPoint() *ReversiPoint {
	r := new(ReversiPoint)
	r.X = 0
	r.Y = 0
	return r
}

////////////////////////////////////////////////////////////////////////////////
///	@brief			ゲッター
///	@fn				GetX() int
///	@return			x int
///	@author			Yuta Yoshinaga
///	@date			2020.07.06
///
////////////////////////////////////////////////////////////////////////////////
func (r ReversiPoint) GetX() int {
	return r.X
}

////////////////////////////////////////////////////////////////////////////////
///	@brief			セッター
///	@fn			 	SetX(x int)
///	@param[in]		x int
///	@return			ありません
///	@author			Yuta Yoshinaga
///	@date			2020.07.06
///
////////////////////////////////////////////////////////////////////////////////
func (r *ReversiPoint) SetX(x int) {
	r.X = x
}

////////////////////////////////////////////////////////////////////////////////
///	@brief			ゲッター
///	@fn				GetY() int
///	@return			y int
///	@author			Yuta Yoshinaga
///	@date			2020.07.06
///
////////////////////////////////////////////////////////////////////////////////
func (r ReversiPoint) GetY() int {
	return r.Y
}

////////////////////////////////////////////////////////////////////////////////
///	@brief			セッター
///	@fn				SetY(y int)
///	@param[in]		y int
///	@return			ありません
///	@author			Yuta Yoshinaga
///	@date			2020.07.06
///
////////////////////////////////////////////////////////////////////////////////
func (r *ReversiPoint) SetY(y int) {
	r.Y = y
}
